package com.lifeservice.model;

import java.util.Date;

public class Server {
	private String serverId;
	private String userId;
	private String serverName;
	private String serverContent;
	private String type;
	private String keyWord;
	private Date updateTime;
	private float jingDu;
	private float weiDu;
	private String province;
	private String city;
	private String memo;
	public String getServerId() {
		return serverId;
	}
	public void setServerId(String serverId) {
		this.serverId = serverId;
	}
	public String getUserId() {
		return userId;
	}
	public void setUserId(String userId) {
		this.userId = userId;
	}
	public String getServerName() {
		return serverName;
	}
	public void setServerName(String serverName) {
		this.serverName = serverName;
	}
	public String getServerContent() {
		return serverContent;
	}
	public void setServerContent(String serverContent) {
		this.serverContent = serverContent;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getKeyWord() {
		return keyWord;
	}
	public void setKeyWord(String keyWord) {
		this.keyWord = keyWord;
	}
	public Date getUpdateTime() {
		return updateTime;
	}
	public void setUpdateTime(Date updateTime) {
		this.updateTime = updateTime;
	}
	public float getJingDu() {
		return jingDu;
	}
	public void setJingDu(float jingDu) {
		this.jingDu = jingDu;
	}
	public float getWeiDu() {
		return weiDu;
	}
	public void setWeiDu(float weiDu) {
		this.weiDu = weiDu;
	}
	public String getProvince() {
		return province;
	}
	public void setProvince(String province) {
		this.province = province;
	}
	public String getCity() {
		return city;
	}
	public void setCity(String city) {
		this.city = city;
	}
	public String getMemo() {
		return memo;
	}
	public void setMemo(String memo) {
		this.memo = memo;
	}
	
	
	
}
